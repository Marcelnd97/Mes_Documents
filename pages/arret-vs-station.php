                    moteur est toujours en marche.</b> S'arrêter au feu, au Stop ou au Cédez le passage de façon régulière 
                    ne constitue pas le sujet de ce chapitre.</p>
                <p><b>Arrêt voulu</b></p>
                <p>Si vous souhaitez vous arrêter en dehors de la zone du feu rouge ou du Stop pour décharger votre
                     véhicule ou déposer un voyageur, le verbe à utiliser reste le même : s'arrêter. 
                     <b>Votre moteur est toujours en marche.</b></p>
                <p>La différence entre l'arrêt imposé par la signalisation ou par le feu et l'arrêt voulu, 
                    c'est que ce dernier risque de gêner les autres. Dans les deux cas, vous n'êtes pas censé quitter le véhicule.</p>
                <p><b>Stationnement voulu : rapide vs. prolongé</b></p>
                <p>Un troisième terme à connaître est le verbe stationner que nous appelons tous communément se garer. 
                Il s'agit de l'immobilisation plus ou moins prolongée du véhicule en l'absence du conducteur. 
                <b>Le moteur doit être arrêté</b> même pour un stationnement de courte durée. Je l'ai appelé voulu, 
