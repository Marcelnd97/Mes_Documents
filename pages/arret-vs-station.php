                <h3><b>Définition des termes</b></h3>
                <b>Arrêt imposé par la signalisation</b>
                <p>Dans les chapitres précédents, je vous ai parlé des feux ou des panneaux Stop ou Cédez 
                    le passage comme étant les régulateurs de trafic. Vous devez anticiper l'arrêt au feu jaune 
