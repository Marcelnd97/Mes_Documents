                <h2 style="font-weight: bold;">Contenu de l'épreuve théorique générale (E.T.G.) </h2>
                <p>Au sein du centre d'examen, vous êtes soumis à une série de 40 questions à choix multiples 
                    diffusées sous la forme d'un diaporama ou sur tablette individuelle. 4 questions sont des
                     mini vidéos. Vous devez répondre à l'aide d'un boitier électronique muni d'un écran ou 
                     directement sur la tablette avec les lettres A, B, C et D, ainsi que les touches 
                     "correction" et "validation". L'épreuve se déroule en présence d'autres candidats
                      et sous la surveillance d'un examinateur. Les questions ne représentent pas une 
                      simple interrogation sur la signalisation et les règles de conduite, mais visent
                       à aiguiser la prise de conscience des risques en conduisant (thèmes C, P, S..). 
                       Pour répondre aux questions vous n'êtes pas considéré comme un conducteur novice.</p>
                <p><b>Déroulement de l'épreuve</b></p>
                <p>Après le contrôle d'identité, vous prenez place dans une salle et attendez que les questions
                     passent pour y répondre. La question est accompagnée d'une photo qui représente une certaine
                      situation de circulation. Vous devez cocher (appuyer sur la télécommande) la ou les bonne(s)
                       réponse(s).</p>
